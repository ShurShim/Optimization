% Лабораторная работа 2
% Вариант 20 (четный)
% Для изготовления двух видов изделий Q1 и Q2 используется три вида сырья S1, S2, S3.
% Запасы сырья, количество единиц сырья, затрачиваемых на изготовление единицы продукции,
% а также величина прибыли, получаемой от реализации единицы продукции, приведены в таблице:
% Вид сырья   Запас сырья   Расход сырья на изделие
%                           Q1   Q2
% S1              21         3    1
% S2              30         2    2
% S3              16         0    3
% Прибыль от ед. изделия      3    2
%
% x = (x1, x2) - количество изделий Q1 и Q2, при котором доход будет максимален
% Как изменится решение в предположении, что х может иметь только целочисленные значения?
%
% Формальная постановка задачи
% цель 3x1 + 2x2 -> max
% / 3x1 + 1x2 <= 21
% | 2x1 + 2x2 <= 30
% | 0x1 + 3x2 <= 16
% \ x1, x2 >= 0
%
% Задача ЛП с ограничениями равенствами
% Двойственная задача z = [bT,hT]y -> min, [AT,E]y >= c, y4,5,6,7,8 >= 0
% Исходная задача F = cTx -> max, Ax=b, 0<=x<=u

clear all;
close all;
clc;

printf("========================================\n");
printf("Лабораторная работа 2. Вариант 20\n");
printf("========================================\n\n");

% Коэффициенты целевой функции (5 переменных: x1, x2, x3, x4, x5)
c = [3; 2; 0; 0; 0];

% Верхние границы для переменных (x1, x2, x3, x4, x5)
u = [15; 15; 60; 80; 35];

% Свободные члены ограничений
b = [21; 30; 16];

% Матрица ограничений (3 уравнения, 5 переменных)
A = [3, 1, 1, 0, 0;
     2, 2, 0, 1, 0;
     0, 3, 0, 0, 1];

printf("========================================\n");
printf("1. Решение прямой задачи (непрерывные переменные)\n");
printf("========================================\n")
[Xopt, Fval] = glpk(c, A, b, [], u, 'SSS', 'CCCCC', -1)

printf("\n========================================\n");
printf("2. Решение задачи целочисленного программирования\n");
printf("========================================\n")
[Xopt_int, Fval_int] = glpk(c, [A; eye(5)], [b; u], [], [], 'SSSUUUUU', 'IIIII', -1)

printf("\n========================================\n");
printf("3. Решение двойственной задачи\n");
printf("========================================\n")
[Yopt, Zval, extra, redcosts] = glpk([b; u], [A', eye(5)], c, [-Inf(1,3), zeros(1,5)], [], 'LLLLL', 'CCCCCCCC')

printf("\n========================================\n");
printf("4. Анализ полученных результатов\n");
printf("========================================\n");
printf("\nПрямая задача (непрерывные переменные):\n");
printf("x1 (Q1) = %.4f\n", Xopt(1));
printf("x2 (Q2) = %.4f\n", Xopt(2));
printf("x3 (доп. переменная S1) = %.4f\n", Xopt(3));
printf("x4 (доп. переменная S2) = %.4f\n", Xopt(4));
printf("x5 (доп. переменная S3) = %.4f\n", Xopt(5));
printf("Максимальная прибыль = %.4f\n\n", Fval);

printf("Целочисленное решение:\n");
printf("x1 (Q1) = %d\n", Xopt_int(1));
printf("x2 (Q2) = %d\n", Xopt_int(2));
printf("x3 (доп. переменная S1) = %d\n", Xopt_int(3));
printf("x4 (доп. переменная S2) = %d\n", Xopt_int(4));
printf("x5 (доп. переменная S3) = %d\n", Xopt_int(5));
printf("Максимальная прибыль = %.4f\n\n", Fval_int);

printf("Двойственная задача:\n");
printf("y1 (оценка ресурса S1) = %.4f\n", Yopt(1));
printf("y2 (оценка ресурса S2) = %.4f\n", Yopt(2));
printf("y3 (оценка ресурса S3) = %.4f\n", Yopt(3));
printf("y4 = %.4f\n", Yopt(4));
printf("y5 = %.4f\n", Yopt(5));
printf("y6 = %.4f\n", Yopt(6));
printf("y7 = %.4f\n", Yopt(7));
printf("y8 = %.4f\n", Yopt(8));
printf("Минимальная стоимость ресурсов = %.4f\n\n", Zval);

printf("Проверка основной теоремы двойственности:\n");
printf("Значение ЦФ прямой задачи = %.4f\n", Fval);
printf("Значение ЦФ двойственной задачи = %.4f\n", Zval);
if abs(Fval - Zval) < 0.0001
    printf("Значения совпадают! Теорема двойственности выполняется.\n");
else
    printf("Значения не совпадают.\n");
endif
