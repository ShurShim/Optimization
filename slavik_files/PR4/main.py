# ============================================================================
# Для U = N (натуральные числа) задаем дискретное нечеткое множество
# Описываем понятие "прохладно для лета" для целых температур

print("\n" + "=" * 70)
print("2. Дискретный случай (U = N)")
print("=" * 70)

# Дискретные значения температуры (целые числа)
U_N = np.arange(U_min, U_max + 1, 1)

# Функция принадлежности для "прохладно для лета"
# Максимальная принадлежность (1) - около 18-19°C (прохладный летний вечер)
# Минимальная (0) - при низких температурах (холодно) и высоких (жарко)
def membership_discrete(t):
    """Функция принадлежности для дискретного случая"""
    if t <= 14:
        return 0.0
    elif t <= 16:
        return 0.3  # еще холодновато для лета
    elif t <= 18:
        return 0.7  # прохладно, но приемлемо
    elif t <= 20:
        return 1.0  # идеально прохладно
    elif t <= 22:
        return 0.8  # слегка теплее
    elif t <= 24:
        return 0.5  # уже не очень прохладно
    elif t <= 26:
        return 0.2  # скорее тепло
    else:
        return 0.0  # жарко

# Вычисляем значения принадлежности
mu_values = np.array([membership_discrete(t) for t in U_N])

# Выводим нечеткое множество в форме записи
print("\nНечеткое множество A для U = N:")
print("A = ", end="")
for i, t in enumerate(U_N):
    if mu_values[i] > 0:
        print(f"{mu_values[i]:.1f}/{t}", end="")
        if i < len(U_N) - 1 and any(mu_values[j] > 0 for j in range(i+1, len(U_N))):
            print(" + ", end="")
print()

# Альтернативная запись с более подробными значениями
