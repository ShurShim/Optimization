# Для непрерывного случая используем трапециевидную (T-образную) функцию
# Так как "прохладно для лета" - это диапазон с постепенным переходом

def membership_continuous(t, a=15, b=17, c=20, d=24):
    """
    T-образная функция принадлежности (трапециевидная)
    a - начало роста (нижняя граница)
    b - начало плато (оптимальная нижняя граница)
    c - конец плато (оптимальная верхняя граница)
    d - конец спада (верхняя граница)
    """
    if t <= a:
        return 0
    elif a < t <= b:
        return (t - a) / (b - a)
    elif b < t <= c:
        return 1
    elif c < t <= d:
        return (d - t) / (d - c)
    else:
        return 0

# Альтернативная функция - колоколообразная (гауссова)
def membership_gaussian(t, mean=18.5, sigma=2.5):
    """
    Гауссова (колоколообразная) функция принадлежности
    mean - среднее значение (наиболее типичная температура)
    sigma - параметр размытости
    """
    return np.exp(-((t - mean) ** 2) / (2 * sigma ** 2))

# Альтернативная функция - S-образная (для прохлады используем Z-образную)
def membership_z_shape(t, a=15, b=24):
    """
    Z-образная функция принадлежности (убывающая)
    a - начало спада
    b - конец спада
    """
    if t <= a:
        return 1
    elif a < t <= b:
        return 1 - (t - a) / (b - a)
    else:
        return 0

# Создаем непрерывную область значений
U_R = np.linspace(U_min, U_max, 500)
mu_trap = np.array([membership_continuous(t) for t in U_R])
mu_gauss = membership_gaussian(U_R)
mu_z = membership_z_shape(U_R)

