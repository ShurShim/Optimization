% ========================================================================
% Практическая работа №2: Нахождение базиса и приведение задачи
% к базисным переменным (вариант 15)
% ========================================================================

clear; clc;

% ---- 1. Исходные данные (система ограничений) ----
% Исходные коэффициенты (5 уравнений, 7 переменных x1..x7)
% Первые два - равенства, затем два неравенства >=, последнее <=
A_orig = [ 7,  26,  17,  17,  25,   7,   3;
          -10,  28,  12,   4,  -7,  -6,  24;
           20,  -6,  18,   4,  24,  -5,  29;
            3,  11,   3,  29,  -3,  26,  -3;
            8,   1,  12,  23,  19,   8,  13 ];

B_orig = [106; 52; 79; 62; 89];

% ---- 2. Приведение к системе уравнений (вводим доп. переменные x8,x9,x10) ----
% Доп. переменные: x8 для 3-го ур-я (>=), x9 для 4-го (>=), x10 для 5-го (<=)
% Для неравенства >= вычитаем доп. переменную; для <= прибавляем.
% Расширенная матрица A (5 строк, 10 столбцов): [x1..x7, x8, x9, x10]
A = zeros(5, 10);
A(:, 1:7) = A_orig;

% 3-е уравнение (>=): вычитаем x8
A(3, 8) = -1;
% 4-е уравнение (>=): вычитаем x9
A(4, 9) = -1;
% 5-е уравнение (<=): прибавляем x10
A(5, 10) = 1;

B = B_orig;

% ---- 3. Подготовка к выбору базиса ----
% Чтобы получить единичную подматрицу для базисных переменных,
% умножим строки 3 и 4 на -1 (тогда коэффициенты при x8 и x9 станут +1)
A(3, :) = -A(3, :);
A(4, :) = -A(4, :);
B(3) = -B(3);
B(4) = -B(4);

% ---- 4. Выбор базисных переменных ----
% Выбираем: x8 (столбец 8), x9 (9), x10 (10), x1 (1), x2 (2)
basis = [8, 9, 10, 1, 2];   % индексы столбцов, входящих в базис
free_vars = setdiff(1:10, basis);  % остальные переменные свободные

% Матрица D (коэффициенты при базисных переменных)
D = A(:, basis);
% Матрица S (коэффициенты при свободных переменных)
S = A(:, free_vars);

% ---- 5. Проверка невырожденности D ----
if abs(det(D)) < 1e-10
    error('Матрица D вырождена. Выберите другой базис.');
else
    printf('Матрица D невырождена, det(D) = %g\n', det(D));
end

% ---- 6. Нахождение обратной матрицы D^-1 и вычисление beta, alpha ----
D_inv = inv(D);
beta = D_inv * B;          % значения базисных переменных при нулевых свободных
alpha = D_inv * S;         % матрица коэффициентов перед свободными переменными

% ---- 7. Вывод результатов ----
printf('\n========== Результаты ==========\n');
printf('Базисные переменные (в порядке столбцов): ');
for i = 1:length(basis)
    printf('x%d ', basis(i));
end
printf('\nСвободные переменные: ');
for i = 1:length(free_vars)
    printf('x%d ', free_vars(i));
end
printf('\n\n');

printf('Вектор beta (значения базисных переменных при свободных = 0):\n');
for i = 1:length(beta)
    printf('  x%d = %g\n', basis(i), beta(i));
end

printf('\nМатрица alpha (коэффициенты при свободных переменных):\n');
printf('(строки соответствуют базисным переменным, столбцы – свободным)\n');
for i = 1:size(alpha,1)
    printf('x%d : ', basis(i));
    for j = 1:size(alpha,2)
        printf(' %+g*x%d', alpha(i,j), free_vars(j));
    end
    printf('\n');
end

% ---- 8. Запись системы в базисных переменных ----
printf('\n========== Система в базисных переменных ==========\n');
for i = 1:length(basis)
    fprintf('x%d = %g', basis(i), beta(i));
    for j = 1:size(alpha,2)
        fprintf(' - (%g)*x%d', alpha(i,j), free_vars(j));
    end
    fprintf('\n');
end

% ---- 9. Анализ допустимости базисного решения ----
% Базисное решение: свободные переменные = 0, базисные = beta
if all(beta >= -1e-10)   % с учётом погрешности вычислений
    printf('\nБазисное решение ДОПУСТИМО (все базисные переменные неотрицательны).\n');
    printf('Опорный план: ');
    x0 = zeros(1,10);
    x0(basis) = beta;
    for i = 1:10
        printf('x%d=%g ', i, x0(i));
    end
    printf('\n');
else
    printf('\nБазисное решение НЕДОПУСТИМО, так как следующие базисные переменные отрицательны:\n');
    for i = 1:length(beta)
        if beta(i) < -1e-10
            printf('  x%d = %g\n', basis(i), beta(i));
        end
    end
    printf('Необходимо выбрать другой базис или применить двухфазный метод.\n');
end

% ---- 10. Дополнительно: можно вывести полное базисное решение ----
printf('\n========== Проверка ==========\n');
% Проверим, что A * X = B при X = [beta; zeros(5,1)] (в порядке всех переменных)
x_full = zeros(10,1);
x_full(basis) = beta;
residual = A * x_full - B;
printf('Невязка A*x - B (должна быть близка к нулю):\n');
disp(residual');
