% Лабораторная работа №3
% Вариант 13

clc;
clear all;
close all;

% Количество абонентов в населенных пунктах (веса узлов графа)
p = [50, 120, 70, 90, 100, 130, 120, 80]

% Расстояния между населенными пунктами (длины ребер графа)
% Матрица имеет вид: начало, конец, длина ребра
edges = [
    1 2 12;
    1 3 7;
    1 5 4;
    2 3 14;
    2 4 5;
    2 5 8;
    3 4 11;
    3 7 3;
    4 5 13;
    4 6 9;
    4 7 15;
    4 8 6;
    5 6 10;
    6 8 14;
    7 8 5;
]

% Количество вершин графа (8 вершин)
n = length(p);

% Построение матрицы кратчайших расстояний (Флойд-Уоршелл)
INF = 1e9;
d = INF * ones(n, n);

% Нули на диагонали
for i = 1:n
    d(i, i) = 0;
end

% Заполняем ребра
for k = 1:size(edges, 1) % от 1 до 15, так как 15 ребер
    i = edges(k, 1);
    j = edges(k, 2);
    w = edges(k, 3);
    d(i, j) = w;
    d(j, i) = w;
end

% Алгоритм Флойда-Уоршелла
for k = 1:n
    for i = 1:n
        for j = 1:n
            if d(i, j) > d(i, k) + d(k, j)
                d(i, j) = d(i, k) + d(k, j);
            end
        end
    end
end

% Вывод матрицы кратчайших путей
fprintf('Матрица кратчайших расстояний (км) - 13 вариант:\n\n');
disp(d);

% Для радиосети (центр графа)
fprintf('\nЗадача 1 - радиосеть\n');
max_row = max(d, [], 2);
fprintf('Максимальные расстояния от каждой вершины:\n');
for i = 1:n
    fprintf('- Из вершины %d: %d\n', i, max_row(i));
end

[min_val, center] = min(max_row);
fprintf('\nУзел радиодоступа нужно разместить в вершине F%d.\n', center);

% Для проводной или кабельной сети (медиана графа)
fprintf('\nЗадача 2 - проводная или кабельная сеть\n');
F = zeros(1, n);
fprintf('Суммы расстояний с учетом количества абонентов:\n');
for i = 1:n
    F(i) = sum(d(i, :) .* p);
    fprintf('- F%d = %d\n', i, F(i));
end

[min_sum, median] = min(F);
fprintf('\nПроводной или кабельный узел нужно разместить в вершине F%d.\n', median);
